    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql="INSERT INTO Medicine_List(Med_ID,Med_Name,Expired_Date,Med_Price,Med_Type,Company,iimage) VALUES(?,?,?,?,?,?,?)";
        try{
            String sw=jTextField4.getText();
            pst=conn.prepareStatement(sql);
            pst.setString(1,jTextField1.getText());
            pst.setString(2,jTextField2.getText());
            pst.setString(3,jTextField3.getText());
            pst.setInt(4,Integer.valueOf(sw));
            pst.setString(5,jComboBox1.getSelectedItem().toString());
            pst.setString(6,jTextField5.getText());
            pst.setBytes(7,med_img);
            
            pst.execute();
            JOptionPane.showMessageDialog(null,"New Med Added");
            pst.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Med is already there");
        }      
    }//GEN-LAST:event_jButton1ActionPerformed

